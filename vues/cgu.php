            <h2>Article 2. Inscription à la plateforme</h2>
            <p>
                Polyphonia est une plateforme en ligne proposant des idées de projets informatiques à réaliser, présenté via 
                un cahier des charges simplifié décrivant les différentes fonctionnalités attendues.<br/>
                L'inscription à la plateforme n'est aucunement obligatoire pour les services et fonctionnalités décrits ci-après, 
                elle devient nécessaire pour les services et fonctionnalités restants, décrits eux aussi ci-après.
            </p>
            <h3>2.1. Accès visiteur</h3>
            <p>
                En tant que visiteur de Polyphonia, vous avez accès à la lecture des différents projets proposés sur la plateforme
                et ce sans limite.<br/>
                Pour chaque projet, une page d'aide dédiée permet de consulter les sujets ayant été ouverts et concernant le projet
                en question. La consultation de ces sujets est ouverte au visiteur de manière illimitée sans restriction.<br/>
                Il est possible de suggérer une modification concernant un sujet, en fournissant pour les membres non inscrits 
                leur adresse mail.<br/>
                Un membre non inscrit peut également proposer un projet via la page dédiée, en fournissant son adresse mail afin d'être
                averti de l'acceptation ou du refus de son idée de projet. Il devra également fournir un pseudonyme qui apparaîtra sur
                la page du projet si celui-ci est accepté et mis en ligne.<br/>
                L'accès au blog et aux articles du blog est également accessible sans inscription et ce sans aucune limite.
            </p>
            <h3>2.2. Accès membre</h3>
            <p>
                Les différents services cités ci-dessus sont également accessibles aux membres inscrits de Polyphonia.<br/>
                L'inscription à la plateforme permet de répondre aux sujets ouverts sur les pages d'aides des projets, ainsi que 
                de voter positivement ou négativement pour une réponse proposée, afin de permettre aux autre visiteurs de la page de
                connaître la validité ou non de cette réponse.<br/>
                Un membre peut également signaler tout contenu qui lui semble ne pas respecter les présentes CGU via les différents boutons
                "Signaler" mis en place sur le site.<br/>
                Un service de messagerie interne est également mis en place sur le site afin de communiquer avec les autres membres du site de 
                manière privée.<br/>
                Un membre inscrit à la possibilité de suivre un projet ou un sujet. Le suivi d'un projet ou d'un sujet permet d'être averti
                par mail des différentes mises à jour effectuées, d'une nouvelle réponse ajoutée ou de la résolution ou fermeture du sujet en question.<br/>
            </p>
            <h3>2.3. Création d'un compte</h3>
            <p>
                La création d'un compte est disponible via le formulaire présent sur la page d'inscription de la plateforme.
                La création de compte sur Polyphonia nécessite de fournir une adresse mail valide, qui sera utilisée pour 
                l'envoi d'un mail de confirmation contenant un code de confirmation d'inscription à rentrer dans le champs dédié
                sur Polyphonia.<br/>
                Il est conseillé d'utiliser un mot de passe à sécurité haute (plus de 6 caractères, pas de noms propres, des caractères spéciaux et une alternance entre minuscule, majuscules, chiffres et lettres)
                lors de votre inscription pour éviter toute connexion à votre compte par une tierce personne.<br/>
                En créant votre compte sur Polyphonia, vous vous engagez à être le seul détenteur de ce compte, toute utilisation
                par un tiers est proscrite, et toute utilisation faite par ce tiers ne respectant pas les présentes CGU vous sera imputée.<br/>
                En cas de perte du mot de passe, vous pourrez suivre la procédure de "Mot de passe oublié" disponible sous le 
                formulaire de connexion à la plateforme.
            </p>
